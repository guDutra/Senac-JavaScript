class Produto {

    constructor(codigo, nome, tipo, descricao, valor, imagem, qtdEstoque, vitrine) {
        this.codigo = codigo;
        this.nome = nome;
        this.tipo = tipo;
        this.descricao = descricao;
        this.valor = valor;
        this.imagem = imagem;
        this.qtdEstoque = qtdEstoque;
        this.vitrine = vitrine;

    }
   

}